def map_product_to_ground(product):
	if (product == Items.Pumpkin):
		return Grounds.Soil
	elif (product == Items.Carrot):
		return Grounds.Soil
	elif (product == Items.Wood):
		return Grounds.Turf
	elif (product == Items.Hay):
		return Grounds.Turf
	elif (product == Items.Power):
		return Grounds.Soil
	elif (product == Items.Gold):
		return Grounds.Turf